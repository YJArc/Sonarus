package com.yjarc.sonarus.RecylerViewAdapters;


public class RoomObject {
    public String creatorName, creatorID, creatorImg, banner, roomTitle, roomDesc;

    public RoomObject(String creatorName, String creatorID, String creatorImg, String banner, String roomTitle, String roomDesc){
        this.creatorName = creatorName;
        this.creatorID = creatorID;
        this.creatorImg = creatorImg;
        this.banner = banner;
        this.roomTitle = roomTitle;
        this.roomDesc = roomDesc;
    }

}
